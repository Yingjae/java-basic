
public class UnaryQ1P65 {

	public static void main(String[] args) {
		int a = 15;
		System.out.println(a--);
		// 1. a값을 콘솔에 찍음 : sysout(a(15))
		// 2. a(15)값을 1 감소시킨 후 저장 : a = a(15) - 1 
		System.out.println(a);
		System.out.println(--a);
		// 1. a(14)값을 1 감소시킨 후 저장 : a = a(14) - 1
		// 2. 콘솔에 찍기 : sysout(a(13))
		System.out.println(a);
	}

}
